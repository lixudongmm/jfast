package com.jfast.core.paging;

import java.util.List;

public class PageBean<T> {
	/**每页的条数*/
	private int limit;
	/**当前页*/
	private int page;
	/**总条数*/
	private int total;
	/**每页的详细数据*/
	private List<T> items;
	public int getLimit() {
		return limit;
	}
	public void setLimit(int limit) {
		this.limit = limit;
	}
	public int getPage() {
		return page;
	}
	public void setPage(int page) {
		this.page = page;
	}
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
	public List<T> getItems() {
		return items;
	}
	public void setItem(List<T> items) {
		this.items = items;
	}
	
}
